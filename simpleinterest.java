package com.demo;

public class simpleinterest {
	public static void main(String[] args) {
      
	//initialized float variables
	 float P = 11.5f;
	 float R = 22.0f;
	 float T = 33.0f;
	 
	 //calculate Simple Interest
	 float simpleinterest = (P*R*T)/100;
	 
	 System.out.println("Simple Interest: "+ simpleinterest);  //display result for simple interest
}
}
